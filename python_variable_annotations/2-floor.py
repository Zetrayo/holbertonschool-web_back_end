#!/usr/bin/env python3
"""
This module contains a function that returns the floor of a float number.
"""


import math

def floor(n: float) -> int:
    """
    Returns the floor of the given float number.

    Parameters:
    n (float): The number to be floored.

    Returns:
    int: The floored integer value of the input float.
    """
    return math.floor(n)
